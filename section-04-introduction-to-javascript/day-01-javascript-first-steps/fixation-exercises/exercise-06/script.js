const a = 16; 
const b = 5;

// Programa para adição:
const adicao = a + b;
console.log("O resultado da soma é : " + adicao);

//Programa para subtração:
const subtracao = a - b;
console.log("O resultado da soma é : " + subtracao);

//Programa para multiplicação:
const multiplicacao = a * b;
console.log("O resultado da multiplicação é : " + multiplicacao);

//Programa para divisão:
const divisao = a / b;
console.log("O resultado da divisão é : " + divisao);

//Programa para módulo:
const modulo = a % b;
console.log("O resultado do módulo é : " + modulo);