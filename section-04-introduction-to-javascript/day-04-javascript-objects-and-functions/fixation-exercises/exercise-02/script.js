//Função de soma

function sum(a, b) {
    return a + b;
};

//Função de subtração

function subtraction(a, b) {
    return a - b;
};

//Função de multiplicação

function multiplication(a, b) {
    return a * b;
};

//Função de divisão

function division(a, b) {
    return a / b;
};

//Função de módulo

function module(a, b) {
    return a % b;
};
